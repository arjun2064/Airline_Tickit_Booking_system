    private void managefleetbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_managefleetbtnActionPerformed
        
        // TODO add your handling code here:
        int getSelectedRow = Listofairlinerstable.getSelectedRow();
        if(getSelectedRow <0){
            JOptionPane.showMessageDialog(null, "No row selected");
        }else{
            airlinerclass al = (airlinerclass) Listofairlinerstable.getValueAt(getSelectedRow, 0);
        ManagefleetJPanel managefleet = new ManagefleetJPanel(userprocesscontainer, al);
        userprocesscontainer.add("ManagefleetJPanel", managefleet);
        CardLayout layout = (CardLayout)userprocesscontainer.getLayout();
        layout.next(userprocesscontainer);
        }
    }//GEN-LAST:event_managefleetbtnActionPerformed


