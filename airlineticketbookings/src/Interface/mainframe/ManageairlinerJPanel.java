    private void deleteairlinerbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteairlinerbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow=Listofairlinerstable.getSelectedRow();

        if (selectedrow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row");
        }
        else{
            int dialogbutton=JOptionPane.YES_NO_OPTION;
            int dialogresult=JOptionPane.showConfirmDialog(null, "will you like to delete this row?");
            if(dialogresult==JOptionPane.YES_OPTION);
            airlinerclass airliner=(airlinerclass)Listofairlinerstable.getValueAt(selectedrow,0);
            ad.removeairliner(airliner);
            JOptionPane.showMessageDialog(null, "Vital Sign has been deleted");
            populateTable();
        
        }
    }//GEN-LAST:event_deleteairlinerbtnActionPerformed

