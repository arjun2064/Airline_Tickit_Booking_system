        public void populateTable()
    {
    
    DefaultTableModel dtm=(DefaultTableModel)flighttable.getModel();
        dtm.setRowCount(0);
        
        for(flightclass flight:a1.getFlightdirectory().getFlightdirectory())
        {
                Object row[]=new Object[9];
                row[0]=flight;
                row[1]=flight.getFlightname();
                row[2]=flight.getSource();
                row[3]=flight.getDestination();
                row[4]=flight.getDeparture();
                row[5]=flight.getArrival();
                row[6]=flight.getFlightcharges();
                row[7]=flight.getAirplane();
                row[8]=flight.getDate();
                dtm.addRow(row);
        }
        
    }

