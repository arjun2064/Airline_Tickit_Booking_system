    private void updatebtninviewflightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtninviewflightsActionPerformed
        // TODO add your handling code here:
        int selectedRow = flighttable.getSelectedRow();
        if(selectedRow <0){
            JOptionPane.showMessageDialog(null, "No row selected");
        }else{
        flightclass updatedflights= (flightclass)flighttable.getValueAt(selectedRow, 0);
        
        flightidtextfield.setText(updatedflights.getFlightID());
        flightnametextfield.setText(updatedflights.getFlightname());
        sourcetextfield.setText(updatedflights.getSource());
        destinationtextfield.setText(updatedflights.getDestination());
        departuretextfield.setText(updatedflights.getDeparture());
        arrivaltextfield.setText(updatedflights.getArrival());
        flightchargestextfield.setText(updatedflights.getFlightcharges());
        Datetextfield.setText(updatedflights.getDate());
     
        
        
        updatebtninviewflights.setVisible(false);
        savebtninviewflights.setVisible(true);
        }
    }//GEN-LAST:event_updatebtninviewflightsActionPerformed

    private void savebtninviewflightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtninviewflightsActionPerformed
        // TODO add your handling code here:
        int selectedRow = flighttable.getSelectedRow();
        flightclass updatedflights= (flightclass)flighttable.getValueAt(selectedRow, 0);
        updatedflights.setFlightID(flightidtextfield.getText());
        updatedflights.setFlightname(flightnametextfield.getText());
        updatedflights.setSource(sourcetextfield.getText());
        updatedflights.setDestination(destinationtextfield.getText());
        updatedflights.setDeparture(departuretextfield.getText());
        updatedflights.setArrival(arrivaltextfield.getText());
        updatedflights.setFlightcharges(flightchargestextfield.getText());
        updatedflights.setDate(Datetextfield.getText());
        
        
        
        
       
        populateTable();
        
        flightidtextfield.setText("");
        flightnametextfield.setText("");
        sourcetextfield.setText("");
        destinationtextfield.setText("");
        departuretextfield.setText("");
        arrivaltextfield.setText("");
        flightchargestextfield.setText("");
        Datetextfield.setText("");
        
        JOptionPane.showMessageDialog(null, "Update Successfull");
        
        updatebtninviewflights.setVisible(true);
        savebtninviewflights.setVisible(false);
    }//GEN-LAST:event_savebtninviewflightsActionPerformed

