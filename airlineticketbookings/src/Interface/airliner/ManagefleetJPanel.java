        public void populateTable()
    {
    
    DefaultTableModel dtm=(DefaultTableModel)listofairplanestable.getModel();
        dtm.setRowCount(0);
        
        for(Airplanes airplanes:al.getFleet().getAirplanedirectory())
        {
                Object row[]=new Object[2];
                row[0]=airplanes;
                row[1]=airplanes.getNumberofseats();
                dtm.addRow(row);
        }
    }
    
    
    

