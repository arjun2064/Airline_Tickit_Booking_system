    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedrow=listofairplanestable.getSelectedRow();

        if (selectedrow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row");
        }
        else{
            int dialogbutton=JOptionPane.YES_NO_OPTION;
            int dialogresult=JOptionPane.showConfirmDialog(null, "will you like to delete this row?");
            if(dialogresult==JOptionPane.YES_OPTION);
            Airplanes airplanes=(Airplanes)listofairplanestable.getValueAt(selectedrow,0);
            al.getFleet().removeairplane(airplanes);
            JOptionPane.showMessageDialog(null, "Vital Sign has been deleted");
            populateTable();
        
        }
                        
        
       
    }//GEN-LAST:event_deletebtnActionPerformed


